package com.videoparaiso.renting.domain;

public class Contenido {
	
	private String nombre;
	private String tipo;

	public Contenido() {
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
}
